/*
   문제: 상품 가격이 저장된 정수형 배열에 대하여 할인율(%)을 입력받아 할인된 가격을 계산해서 출력하는 프로그램을 작성하시오.
        상품 가격이 저장된 배열의 크기는 5이고, 상품 가격은 입력받아서 사용한다.
        할인된 가격은 별도의 배열에 저장해야 한다.
   출력예시: 상품가 5개를 입력하세요:
          12000 18000 20000 50000 5600
          할인율(%)? 20
          가격:  12000 --> 할인가:   9600
          가격:  18000 --> 할인가:  14400
          가격:  20000 --> 할인가:  16000
          가격:  50000 --> 할인가:  40000
          가격:   5600 --> 할인가:   4480
*/
#define _CRT_SECURE_NO_WARNINGS // scanf 보안 경고를 무시하기 위한 매크로
#include <stdio.h> // 표준 입출력 함수 사용
#define ARR_SIZE 5 // 배열 크기를 상수로 정의 (상품 5개)

// 함수 선언 (메인 함수보다 아래에 정의되어 있으므로 미리 선언)
void product_dis();

int main(void)
{
    product_dis(); // 가격 입력 및 할인 적용 기능 실행
    return 0; // 프로그램 정상 종료
}

void product_dis()
{
    int i, discount_rate; // 반복문 변수 i, 할인율 변수 선언
    int arr[ARR_SIZE]; // 원래 상품가 저장 배열
    int arr_dis[ARR_SIZE]; // 할인된 가격 저장 배열

    printf("상품가 5개를 입력하세요:\n");

    // 상품 가격 5개 입력 받기
    for (i = 0; i < ARR_SIZE; i++)
    {
        scanf("%d", &arr[i]); // 사용자로부터 상품 가격 입력받아 arr 배열에 저장
    }

    printf("할인율(%%)? "); // %%는 % 한 개를 출력하기 위한 escape
    scanf("%d", &discount_rate); // 할인율 입력받기 (예: 20 → 20%)

    // 입력된 할인율을 이용해 할인된 가격 계산
    // arr[i] / 100 * (100 - discount_rate)
    // 전체 가격을 100으로 나눈 뒤, (100 - 할인율%)을 곱해 할인 적용
    for (i = 0; i < ARR_SIZE; i++)
    {
        arr_dis[i] = (arr[i] / 100) * (100 - discount_rate);
    }

    // 원래 가격과 할인된 가격을 보기 좋게 출력
    for (i = 0; i < ARR_SIZE; i++)
    {
        printf("가격:%7d --> 할인가:%7d\n", arr[i], arr_dis[i]);
        // %7d : 숫자를 오른쪽 정렬하여 7칸 공간 확보
    }

}